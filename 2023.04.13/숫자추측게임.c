#define _CRT_SECURE_NO_WARNINGS
#include <stdio.h>
#include <stdlib.h>
#include <time.h>

int main()
{
	srand((unsigned)time(NULL));
	int answer = rand() % 100; //정답을 난수로 발생한다.
	int guess;
	int tries = 0;
	printf("%d", answer);
	// 반복 구조
	do {
		printf("정답을 추측하여 보시오: ");
		scanf("%d", &guess);
		tries++;

		if (guess > answer) //사용자가 입력한 정수가 정답보다 높으면
			printf("high \n");
		if (guess < answer) //사용자가 입력한 정수가 정답보다 낮으면
			printf("low \n");
	} while (guess != answer);

	printf("축하합니다. 시도횟수=%d\n", tries);

	return 0;


}